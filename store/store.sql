------------------------------------------------------------------------------
-- The `store` schema
------------------------------------------------------------------------------
CREATE SCHEMA IF NOT EXISTS store;
------------------------------------------------------------------------------
------------------------------------------------------------------------------
-- function to automatically update the `updated_at` column
------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION store.update_updated_at_column()
    RETURNS TRIGGER AS
$$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';
